    private void butTAKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTAKActionPerformed
        butPomoc.setVisible(false);
        Aktualizuj();

        if (licznik_tak == 0) {
            butTAK.setText("Dalej");
            TabbedPane.setVisible(true);
            PokazPrzyciski();
            txtOut.setText("Witam w Pogramie......Aby zakupić bilet wciśnij przycisk DALEJ");
        } else {
            if (licznik_tak == 1) {
                if (granica == licznik_bilet) {
                    cena = cena / 2;
                    granica = 10;
                }
                txtOut.setText("Należy poczekać na wyciąg!");
            } else if (licznik_tak == 2) {
                ChowajPrzyciski();
                txtOut.setText("Oczekiwanie........");
                tiempo.start();
            } else if (licznik_tak == 3) {
                licznik_bilet++;
                Random y = new Random();
                int g = r.nextInt(3);

                switch (g) {
                    case 0: {
                        Klient klient1 = new Klient(cena);
                        log = log + "Zakup: " + licznik_bilet + " " + "Kto: " + "Klient " + "Nazwisko: " + klient1.getNazwisko() + " " + "Imie: "
                                + klient1.getImie() + " " + "Przedział: " + klient1.getPrzedzial() + " " + "Województwo: " + klient1.getWojewodztwo() + " " + "Pracownik: " + pracownik.getImie() + " " + pracownik.getNazwisko() + " " + " " + "Cena biletu: " + klient1.getCena() + "////         ";
                    }
                    break;
                    case 1: {
                        Uczestnik klient1 = new Uczestnik(cena);
                        log = log + "Zakup: " + licznik_bilet + " " + "Kto: " + "Uczestnik " + "Nazwisko: " + klient1.getNazwisko() + " " + "Imie: "
                                + klient1.getImie() + " " + "Przedział: " + klient1.getPrzedzial() + " " + "Sprzęt: " + klient1.getSprzet() + " " + "Pracownik: " + pracownik.getImie() + " " + pracownik.getNazwisko() + " " + " " + "Cena biletu: " + klient1.getCena() + "////         ";
                    }
                    break;
                    case 2: {
                        Zwiedzajacy klient1 = new Zwiedzajacy(cena);
                        log = log + "Zakup: " + licznik_bilet + " " + "Kto: " + "Zwiedzajacy " + "Nazwisko: " + klient1.getNazwisko() + " " + "Imie: "
                                + klient1.getImie() + " " + "Przedział: " + klient1.getPrzedzial() + " " + "Miejsce: " + klient1.getMiejsce() + " " + "Pracownik: " + pracownik.getImie() + " " + pracownik.getNazwisko() + " "
                                + " " + "Cena biletu: " + klient1.getCena() + "////         ";
                    }
                    break;

                }
                txtBaza.setText(log);
                txtOut.setText("Bilet Został zakupiony : informacje w części - Baza -> Log -              Aby zakupić kolejny proszę wcisnąć przycisk - Powtórz -");
                butTAK.setText("Powtórz");
                licznik_tak = -1;

            } else {

            }
        }
        licznik_tak++;
    }//GEN-LAST:event_butTAKActionPerformed

