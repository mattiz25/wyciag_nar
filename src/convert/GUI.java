    private void butGranicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butGranicaActionPerformed

        try {
            granica = Integer.parseInt(txtInGranica.getText());
            if (granica < 0) {
                granica = (granica * (-1));
            }
            if (granica == 0) {
                granica = 1;
            }
            Aktualizuj();
            butGranica.setSelected(false);
            txtInGranica.setText("");
            licznik_bilet = 0;
        } catch (NumberFormatException e) {
            txtOut.setText("Musi być liczba!");
            licznik_tak = 0;
            butNIE.setVisible(false);
            butTAK.setText("OK");
            butGranica.setSelected(false);
        }
//Przycisk umożliwający zmianę granicy
        // TODO add your handling code here:
    }//GEN-LAST:event_butGranicaActionPerformed

